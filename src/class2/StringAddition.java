package class2;

public class StringAddition {
    public static void main(String[] args) {
        String firstName="George";
        String lastName="Michael";

        System.out.println(firstName+" "+lastName);
        System.out.println("*********"+firstName+" "+lastName+"*********");

        /*
        Java execute codes from top to bottom but if we have multiple operations on the same lines, it executes them
        from left to right
         */

        System.out.println("10"+"10");//concatenate two strings


    }
}

