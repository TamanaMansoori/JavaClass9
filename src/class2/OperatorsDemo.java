package class2;

public class OperatorsDemo {
    public static void main(String[] args) {
        int number1=10;
        int number2=20;
        System.out.println(number1+number2);
        System.out.println(number1-number2);
        System.out.println(number1*number2);
        System.out.println(number1/number2);//here the actual result is (0.5) but we get (0) on councle because the data type we used is (int)
        /*
        if you have to store whole numbers always use (int)
        if you have to store decimal numbers always use (double)
        if you have to store more than one character, you can use (String).
         */



    }
}
