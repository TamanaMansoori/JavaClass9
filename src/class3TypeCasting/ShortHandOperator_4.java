package class3TypeCasting;

public class ShortHandOperator_4 {
    public static void main(String[] args) {

        int number=10;
        number=number+10;
        System.out.println(number);

        number+=10;// it means whatever value is stored in number variable add 10 to it. here is the shortcut for line 7
        System.out.println(number);
        number+=20;
        System.out.println(number);

        number*=10;
        System.out.println(number);

        int number2=20;
        number2-=10;
        System.out.println(number2);

        int number3=100;
        number3/=10;
        System.out.println(number3);


    }
}
